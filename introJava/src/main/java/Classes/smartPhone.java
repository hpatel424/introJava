package Classes;

public class smartPhone {
    private String brand = "samsung";
    private String model = "s10";

    public smartPhone() {
        this.brand = brand;
    }

    public smartPhone(String model) {
        this.model = model;
    }

    public String getBrand() {
        return brand;
    }
    public String getModel() {
        return model;
    }
}
